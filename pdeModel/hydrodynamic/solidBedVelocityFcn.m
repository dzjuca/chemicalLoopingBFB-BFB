function us = solidBedVelocityFcn(ub, alpha, reactorType, Global)
% -------------------------------------------------------------------------
    % solidBedVelocityFcn function 
    % ----------------------------| input |--------------------------------
    % ----------------------------| output |-------------------------------            
% -------------------------------------------------------------------------

    fw   = Global.(reactorType).fDynamics.fw;

% -------------------------------------------------------------------------

    us = ((fw.*alpha.*ub)./(1 - alpha - alpha.*fw));
    
% -------------------------------------------------------------------------
end
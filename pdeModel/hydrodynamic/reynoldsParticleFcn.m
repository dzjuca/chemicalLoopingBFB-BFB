function Re_p = reynoldsParticleFcn(rho_g, dp, usg, mu_g)
% -------------------------------------------------------------------------
    % reynoldsParticleFcn - function
    % ----------------------------| input |--------------------------------
    % ----------------------------| output |-------------------------------
    % ---------------------------------------------------------------------
% --------------------| constants values |---------------------------------
% -------------------------------------------------------------------------

    Re_p = (rho_g * dp * usg) / mu_g;
    
% -------------------------------------------------------------------------
end
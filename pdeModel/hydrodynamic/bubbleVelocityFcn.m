function ub = bubbleVelocityFcn(umf, db, reactorType, Global)
% -------------------------------------------------------------------------
    % bubbleVelocityFcn function 
    % ----------------------------| input |--------------------------------
    % ----------------------------| output |-------------------------------            
% ------------------------------------------------------------------------- 

    usg0 = Global.(reactorType).fDynamics.usg0;
    g    = Global.g;
    ub   = (usg0 - umf) + 0.711.*(g.*db).^(0.5);
    
% ------------------------------------------------------------------------- 
end